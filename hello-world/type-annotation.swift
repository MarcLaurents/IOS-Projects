// We can provide a type annotation 
// when we declare a variable to be 
// clear about the kind of value that 
// the variable can store.
// Some of the basic data types:
// Int: integer numbers
// Double: floating-point numbers
// String: a sequence of characters
// Bool: true/false values
// Type annotations begin with a colon : and end with a type.
var artist: String
artist = "Daniel Johnston"
var book: String = "Just Kids"
var pageCount: Int = 278